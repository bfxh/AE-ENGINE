#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub struct Triangle {
    pub v0: Vec3,
    pub v1: Vec3,
    pub v2: Vec3,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MarchingCubesMesh {
    pub vertices: Vec<Vec3>,
    pub triangles: Vec<Triangle>,
}

#[derive(Debug, Clone, Copy)]
pub struct VoxelGrid {
    pub width: usize,
    pub height: usize,
    pub depth: usize,
    pub voxel_size: f32,
    pub origin: Vec3,
}

impl VoxelGrid {
    pub fn new(width: usize, height: usize, depth: usize, voxel_size: f32, origin: Vec3) -> Self {
        Self { width, height, depth, voxel_size, origin }
    }

    pub fn total_cells(&self) -> usize {
        (self.width + 1) * (self.height + 1) * (self.depth + 1)
    }

    pub fn index(&self, x: usize, y: usize, z: usize) -> usize {
        z * (self.width + 1) * (self.height + 1) + y * (self.width + 1) + x
    }

    pub fn position(&self, x: usize, y: usize, z: usize) -> Vec3 {
        Vec3::new(
            self.origin.x + x as f32 * self.voxel_size,
            self.origin.y + y as f32 * self.voxel_size,
            self.origin.z + z as f32 * self.voxel_size,
        )
    }
}

