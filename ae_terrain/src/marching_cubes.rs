const CORNER_INDEX_A_FROM_EDGE: [usize; 12] = [0, 1, 2, 3, 4, 5, 6, 7, 0, 1, 2, 3];
const CORNER_INDEX_B_FROM_EDGE: [usize; 12] = [1, 2, 3, 0, 5, 6, 7, 4, 4, 5, 6, 7];

fn vertex_interp(isolevel: f32, p1: Vec3, p2: Vec3, val1: f32, val2: f32) -> Vec3 {
    if (isolevel - val1).abs() < 1e-6 {
        return p1;
    }
    if (isolevel - val2).abs() < 1e-6 {
        return p2;
    }
    if val1 == val2 {
        return p1;
    }
    let mu = (isolevel - val1) / (val2 - val1);
    Vec3::new(p1.x + mu * (p2.x - p1.x), p1.y + mu * (p2.y - p1.y), p1.z + mu * (p2.z - p1.z))
}

pub fn generate_mesh(grid: &VoxelGrid, density: &[f32], isolevel: f32) -> MarchingCubesMesh {
    let mut vertices = Vec::new();
    let mut triangles = Vec::new();
    let w = grid.width;
    let h = grid.height;
    let d = grid.depth;

    for z in 0..d {
        for y in 0..h {
            for x in 0..w {
                let mut cube_vals = [0.0f32; 8];
                let mut cube_pos = [Vec3::ZERO; 8];
                let cube_idx = [
                    grid.index(x, y, z),
                    grid.index(x + 1, y, z),
                    grid.index(x + 1, y, z + 1),
                    grid.index(x, y, z + 1),
                    grid.index(x, y + 1, z),
                    grid.index(x + 1, y + 1, z),
                    grid.index(x + 1, y + 1, z + 1),
                    grid.index(x, y + 1, z + 1),
                ];

                for i in 0..8 {
                    cube_vals[i] = density[cube_idx[i]];
                    let (cx, cy, cz) = match i {
                        0 => (x, y, z),
                        1 => (x + 1, y, z),
                        2 => (x + 1, y, z + 1),
                        3 => (x, y, z + 1),
                        4 => (x, y + 1, z),
                        5 => (x + 1, y + 1, z),
                        6 => (x + 1, y + 1, z + 1),
                        7 => (x, y + 1, z + 1),
                        _ => unreachable!(),
                    };
                    cube_pos[i] = grid.position(cx, cy, cz);
                }

                let mut cube_index = 0usize;
                for (i, &val) in cube_vals.iter().enumerate().take(8) {
                    if val < isolevel {
                        cube_index |= 1 << i;
                    }
                }

                let edge_flags = EDGE_TABLE[cube_index];
                if edge_flags == 0 {
                    continue;
                }

                let mut edge_verts = [Vec3::ZERO; 12];
                for i in 0..12 {
                    if edge_flags & (1 << i) != 0 {
                        let a = CORNER_INDEX_A_FROM_EDGE[i];
                        let b = CORNER_INDEX_B_FROM_EDGE[i];
                        edge_verts[i] = vertex_interp(
                            isolevel,
                            cube_pos[a],
                            cube_pos[b],
                            cube_vals[a],
                            cube_vals[b],
                        );
                    }
                }

                let mut tri_idx = 0;
                while tri_idx < 15 && TRI_TABLE[cube_index][tri_idx] != -1 {
                    let v0 = edge_verts[TRI_TABLE[cube_index][tri_idx] as usize];
                    let v1 = edge_verts[TRI_TABLE[cube_index][tri_idx + 1] as usize];
                    let v2 = edge_verts[TRI_TABLE[cube_index][tri_idx + 2] as usize];

                    vertices.push(v0);
                    vertices.push(v1);
                    vertices.push(v2);
                    let n = vertices.len();
                    triangles.push(Triangle {
                        v0: vertices[n - 3],
                        v1: vertices[n - 2],
                        v2: vertices[n - 1],
                    });

                    tri_idx += 3;
                }
            }
        }
    }

    MarchingCubesMesh { vertices, triangles }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_voxel_grid_index() {
        let grid = VoxelGrid::new(4, 4, 4, 1.0, Vec3::ZERO);
        assert_eq!(grid.index(0, 0, 0), 0);
        assert_eq!(grid.index(1, 0, 0), 1);
        assert_eq!(grid.index(0, 1, 0), 5);
        assert_eq!(grid.index(0, 0, 1), 25);
    }

    #[test]
    fn test_empty_mesh() {
        let grid = VoxelGrid::new(4, 4, 4, 1.0, Vec3::ZERO);
        let density = vec![1.0f32; grid.total_cells()];
        let mesh = generate_mesh(&grid, &density, 0.5);
        assert!(mesh.triangles.is_empty());
    }

    #[test]
    fn test_sphere_mesh() {
        let grid = VoxelGrid::new(8, 8, 8, 1.0, Vec3::new(-4.0, -4.0, -4.0));
        let mut density = vec![0.0f32; grid.total_cells()];
        for z in 0..=grid.depth {
            for y in 0..=grid.height {
                for x in 0..=grid.width {
                    let pos = grid.position(x, y, z);
                    density[grid.index(x, y, z)] = 3.0 - pos.length();
                }
            }
        }
        let mesh = generate_mesh(&grid, &density, 0.0);
        assert!(!mesh.vertices.is_empty());
        assert!(!mesh.triangles.is_empty());
    }

    #[test]
    fn test_full_mesh() {
        let grid = VoxelGrid::new(4, 4, 4, 1.0, Vec3::ZERO);
        let density = vec![-1.0f32; grid.total_cells()];
        let mesh = generate_mesh(&grid, &density, 0.5);
        assert!(mesh.triangles.is_empty());
    }
}
